def iso_2_to_3(iso2):
    country = pycountry.countries.get(alpha_2=iso2)

    if(country is None):
        return np.nan
    else:
        return country.alpha_3

def iso_to_country(iso):
    country_name = iso_to_country_manual.get(iso, None)

    if country_name is None:
        country = pycountry.countries.get(alpha_2=iso)

        if(country is None):
            return None
        else:
            country_name = country.name

    return country_name

def country_to_iso(country_name):
    country = pycountry.countries.get(name=country_name)

    if country is None:
        return country_to_iso_manual.get(country_name, None)
    else:
        return country.alpha_2

